import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import peggy from 'peggy';

const { generate } = peggy;

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');
const sourcePath = resolve(root, 'projects/ccd-case-ui-toolkit/src/lib/shared/directives/conditional-show/services/condition.peggy');
const outputPath = resolve(root, 'projects/ccd-case-ui-toolkit/src/lib/shared/directives/conditional-show/services/condition-parser.generated.ts');

const grammar = readFileSync(sourcePath, 'utf8');
const parserSource = generate(grammar, {
  output: 'source',
  format: 'es',
});

writeFileSync(outputPath, [
  '/* eslint-disable */',
  '// @ts-nocheck',
  '// This file is generated by scripts/generate-condition-parser.mjs. Do not edit by hand.',
  parserSource.trimEnd(),
  'const parser = { SyntaxError: peg$SyntaxError, parse: peg$parse };',
  'export default parser;',
  '',
].join('\n'));
